package Settwo;

public class OddNum {

	public static void main(String[] args) {
		
		  int[]  numbers = { 1,22,23,56,78, 34,33};
	        
	        System.out.println("Odd numbers from the list of arrays are: ");
	        
	      
	        for (int i = 0; i < numbers.length; i++) {
	        	
	            if (numbers[i] % 2 == 1) {
	            	
	                System.out.println(numbers[i]);
	        	}
	        }
	        	
	        		

	}

}
