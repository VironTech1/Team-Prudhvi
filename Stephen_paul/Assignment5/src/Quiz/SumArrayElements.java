package Quiz;

public class SumArrayElements {

	public static void main(String[] args) {
		        // Given array
		        int[] array = {5, 10, 15, 20, 25};

		        // Calculate the sum
		        int sum = 0;
		        for (int i = 0; i < array.length; i++) {
		            sum += array[i];
		        }

		        // Print the sum
		        System.out.println("Sum of array elements: " + sum);
		    }


	}

