public class InheritanceExample {
		    public static void main(String[] args) {
		        Cat myCat = new Cat();
		        Dog myDog = new Dog();
		        Goat myGoat = new Goat();

		        // Using inherited methods
		        myCat.run();
		        myCat.eat();
		        myDog.run();
		        myDog.eat();
		        myGoat.run();
		        myGoat.eat();
		    }


	}


